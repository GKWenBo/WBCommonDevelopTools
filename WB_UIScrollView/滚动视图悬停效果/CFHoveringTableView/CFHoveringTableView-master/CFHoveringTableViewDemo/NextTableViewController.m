- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell* cell = [tableView dequeueReusableCellWithIdentifier:@"nextCell"];
    if (!cell)
    {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:@"nextCell"];
    }
    cell.textLabel.text = [NSString stringWithFormat:@"next -%zd-", indexPath.row];
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    CFHoveringTableViewController* hoveringVC = [[CFHoveringTableViewController alloc] init];
    [self.navigationController pushViewController:hoveringVC animated:YES];
}
